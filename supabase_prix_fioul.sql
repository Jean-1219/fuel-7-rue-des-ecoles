-- ============================================================
-- Table pour stocker les prix hebdomadaires DGEC du fioul
-- À coller dans Supabase > SQL Editor > New query
-- ============================================================

CREATE TABLE IF NOT EXISTS prix_fioul_dgec (
  id BIGSERIAL PRIMARY KEY,
  date DATE NOT NULL UNIQUE,  -- une entrée par semaine
  prix_litre NUMERIC(6,4) NOT NULL,  -- prix en €/L
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS prix_fioul_dgec_date_idx ON prix_fioul_dgec (date);

ALTER TABLE prix_fioul_dgec ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Accès public prix_fioul_dgec" ON prix_fioul_dgec
  FOR ALL USING (true) WITH CHECK (true);

